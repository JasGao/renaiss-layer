export function getPortfolioCards(): LayerCard[] {
  return MOCK_CARDS.filter(
    (c) => !c.hiddenFromPortfolio && c.nft.ownerLabel === "You",
  );
}

export function getCardById(id: string): LayerCard | undefined {
  return MOCK_CARDS.find((c) => c.id === id);
}

export function getCardByTokenId(tokenId: string): LayerCard | undefined {
  return MOCK_CARDS.find((c) => c.tokenId === tokenId);
}

export function getEligibleRedemptionCards(): LayerCard[] {
  return MOCK_CARDS.filter((c) => c.redeemable && !c.hiddenFromPortfolio);
}
